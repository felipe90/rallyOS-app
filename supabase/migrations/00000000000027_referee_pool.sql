ALTER TABLE athlete_stats 
ADD COLUMN IF NOT EXISTS matches_refereed INTEGER DEFAULT 0;

COMMENT ON COLUMN athlete_stats.matches_refereed IS 
'Total matches refereed by this player (for round-robin balancing)';

-- 6. FUNCTION to update matches_refereed when assignment is confirmed
CREATE OR REPLACE FUNCTION update_referee_stats()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.is_confirmed = TRUE AND OLD.is_confirmed = FALSE THEN
        -- Increment matches_refereed for the referee
        UPDATE athlete_stats
        SET matches_refereed = matches_refereed + 1
        WHERE person_id = (
            SELECT id FROM persons WHERE user_id = NEW.user_id
        );
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_update_referee_stats ON referee_assignments;
CREATE TRIGGER trg_update_referee_stats
AFTER UPDATE ON referee_assignments
FOR EACH ROW
EXECUTE FUNCTION update_referee_stats();

