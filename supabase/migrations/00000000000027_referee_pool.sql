-- 3. CREATE available_referees VIEW
-- Returns users who are checked-in, have a user_id, and are NOT playing in the match
CREATE OR REPLACE VIEW available_referees AS
WITH match_players AS (
    -- Get all player user_ids for each match
    SELECT 
        m.id AS match_id,
        ARRAY_AGG(DISTINCT p.user_id) FILTER (WHERE p.user_id IS NOT NULL) AS playing_user_ids
    FROM matches m
    JOIN categories c ON c.id = m.category_id
    JOIN tournament_entries te ON te.category_id = c.id
    JOIN entry_members em ON em.entry_id = te.id
    JOIN persons p ON p.id = em.person_id
    WHERE te.checked_in_at IS NOT NULL
    GROUP BY m.id
)
SELECT DISTINCT ON (mp.match_id, rv.user_id)
    mp.match_id,
    p.user_id,
    p.id AS person_id,
    t.id AS tournament_id,
    t.name AS tournament_name,
    c.name AS category_name,
    m.round_name,
    -- Count how many confirmed matches this user has refereed
    COALESCE(
        (SELECT COUNT(*) 
         FROM referee_assignments ra2 
         WHERE ra2.user_id = p.user_id AND ra2.is_confirmed = TRUE),
        0
    ) AS matches_refereed
FROM match_players mp
CROSS JOIN LATERAL unnest(mp.playing_user_ids) WITH ORDINALITY AS pp(user_id, ord)
JOIN matches m ON m.id = mp.match_id
JOIN categories c ON c.id = m.category_id
JOIN tournaments t ON t.id = c.tournament_id
JOIN persons p ON p.user_id = pp.user_id
JOIN tournament_entries te ON te.category_id = c.id 
    AND te.checked_in_at IS NOT NULL
    AND EXISTS (
        SELECT 1 FROM entry_members em2 
        WHERE em2.entry_id = te.id AND em2.person_id = p.id
    )
JOIN referee_volunteers rv ON rv.person_id = p.id 
    AND rv.tournament_id = t.id 
    AND rv.is_active = TRUE
WHERE p.user_id != ALL(COALESCE(mp.playing_user_ids, ARRAY[]::UUID[]))
  AND NOT EXISTS (
      SELECT 1 FROM referee_assignments ra
      WHERE ra.match_id = mp.match_id AND ra.is_confirmed = TRUE
  )
ORDER BY mp.match_id, rv.user_id, matches_refereed ASC;

COMMENT ON VIEW available_referees IS 
'Players available to referee a specific match. Excludes: (1) non-checked-in, (2) no user_id, (3) playing in match, (4) already assigned to another match';

